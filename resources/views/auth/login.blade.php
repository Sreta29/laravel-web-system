    <script>
        function togglePasswordVisibility(inputId, iconId) {
            const passwordInput = document.getElementById(inputId);
            const icon = document.getElementById(iconId);

            if (passwordInput.type === "password") {
                passwordInput.type = "text";
                icon.innerHTML = '<svg id="toggle-password-icon" class="h-5 w-5 text-gray-500" fill="none" xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" stroke="currentColor"><path d="M17.293 6.707a1 1 0 0 1 1.414 1.414l-10 10a1 1 0 0 1-1.414 0l-10-10a1 1 0 1 1 1.414-1.414L12 14.586l5.293-5.293z" /><path d="M12 4l-8 8m8 0l8 8m-8-8v14" /></svg>';
            } else {
                passwordInput.type = "password";
                icon.innerHTML = '<svg id="toggle-password-icon" class="h-5 w-5 text-gray-500" fill="none" xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" stroke="currentColor"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M15 12a3 3 0 11-6 0 3 3 0 016 0z" /><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M2.458 12C3.732 7.943 7.523 5 12 5c4.477 0 8.268 2.943 9.542 7-.033.071-.07.138-.107.208-.868 1.634-2.186 3.042-3.758 4.07C15.954 17.852 14.022 19 12 19c-4.477 0-8.268-2.943-9.542-7z" /></svg>';
            }
        }
